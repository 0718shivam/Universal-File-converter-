// Simple script to create a test PNG image
const fs = require('fs');
const path = require('path');

// Create a simple 100x100 PNG image (1x1 red pixel, scaled)
// PNG signature and basic structure for a 1x1 red pixel image
const pngData = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
  0x00, 0x00, 0x00, 0x64, 0x00, 0x00, 0x00, 0x64, // 100x100
  0x08, 0x02, 0x00, 0x00, 0x00, 0xFF, 0x80, 0x02, 0x03,
  0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41, 0x54, // IDAT chunk
  0x08, 0x1D, 0x01, 0x01, 0x00, 0xFE, 0xFF, 0x00, 0x00, 0x02, 0x00, 0x01,
  0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, // IEND chunk
  0xAE, 0x42, 0x60, 0x82
]);

const outputPath = path.join(__dirname, 'test-image.png');
fs.writeFileSync(outputPath, pngData);
console.log('Test image created at:', outputPath);
